public class MathOps
{
    public static void main(String[] args)
    {
        double d1 = 4.61712;
        double d2 = -3.23;

        System.out.printf("The value of d1 is: %.2f\n", d1);
        System.out.printf("The value of d1 is: %5.2f\n", d1);
        System.out.printf("The value of d1 is: %5.0f\n", d1);
    }
}
