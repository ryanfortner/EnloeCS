public class VerticalAddition
{
    public static void main(String[] args)
    {
        // Print addition of 2 one-digit numbers. uses \n for newline formatting.
        System.out.print(" 4\n+3\n--\n 7\n");
        
        // Print a lone newline to separate the operations
        System.out.print("\n");
        
        // Print addition of 2 two-digit numbers.
        System.out.print(" 14\n+27\n---\n 41\n");
    }
}
